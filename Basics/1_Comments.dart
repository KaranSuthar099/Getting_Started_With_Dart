void main() {
  // Single line comments in Dart start with Double-Backward-Slash (//)
  // this is a single line comment

  // Multiline Comments in Dart start and end with a Backward-Slash-Star(/* */)
  /* This
     is a
     Multiline Comment
   */

  // Documentation comments in dart start with a triple Backward-Slash (///)
  /// this is a documentation comment
}
